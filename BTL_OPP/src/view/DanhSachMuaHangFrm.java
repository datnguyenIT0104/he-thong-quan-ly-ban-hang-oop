    private void initTable(){// khởi tạo 
        
        // Hien thi ten
        LabTen.setText( listMuaMH.getKhach().getHoTen());
        
        // Tạo bảng
        String[] Colums = new String[]{
            "STT", "Mã Mặt Hàng" ,"Tên", "Số Lượng" , "Ghi Chú"
        };
        model.setColumnIdentifiers(Colums);
        if( listMuaMH.getListVMH().size() == 0){
            tblViecMuaHang.setModel(model);
            
        }else{
            int index = 1;
            for (ViecMuaHang viecMuaHang : listMuaMH.getListVMH()) {
                model.addRow(new Object[]{ index++, viecMuaHang.getMatHang().getId(),
                viecMuaHang.getMatHang().getTen(), viecMuaHang.getSoLuong(), viecMuaHang.getGhiChu()});
            }
            tblViecMuaHang.setModel(model);
            
        }
            
    }
    private void fillTable(){// load lại bảng
        model.setRowCount(0);
        
        int index = 1;
        for (ViecMuaHang viecMuaHang : listMuaMH.getListVMH()) {
            model.addRow(new Object[]{ index++, viecMuaHang.getMatHang().getId(),
            viecMuaHang.getMatHang().getTen(), viecMuaHang.getSoLuong(), viecMuaHang.getGhiChu()});
        }
        model.fireTableDataChanged();
    }
    private void themDuuLieuHoaDon(){
        // đóng gói dữ liệu hóa đơn
        
        Date now = new Date();
        listMuaMH.setThoiGianMua(now);
        listMuaMH.setDaThanhToan(false);
        listMuaMH.tinhKhuyenMai();
        
    }
