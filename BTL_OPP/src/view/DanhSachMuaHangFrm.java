    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if( listMuaMH.getListVMH().size() == 0){
            JOptionPane.showMessageDialog(this, "Danh sách mua hàng trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // thêm hóa đơn vào CSDL trước
        HoaDonDAO hdd = new HoaDonDAO();
        hdd.themHoaDon(listMuaMH);
        
        // thêm việc mua hàng vào CSDL
        
        ViecMuaHangDAO vmhD = new ViecMuaHangDAO();
        vmhD.themViecMuaHang( listMuaMH);
        JOptionPane.showMessageDialog(this, "Lưu thành công!");
        
        (new XuatHoaDonFrm(listMuaMH)).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnXoaMatHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaMatHangActionPerformed
        int rowSelected = tblViecMuaHang.getSelectedRow();
        if( rowSelected >= 0){
            int idMatHang = (int) tblViecMuaHang.getValueAt(rowSelected, 1);
            
            for (ViecMuaHang viecMuaHang : listMuaMH.getListVMH()) {
                if( viecMuaHang.getMatHang().getId() == idMatHang){
                    int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn xóa sản phẩm có mã " +
                            idMatHang, "Xác nhận", JOptionPane.YES_NO_OPTION);
                    if( choice == JOptionPane.NO_OPTION) break;
                    listMuaMH.getListVMH().remove(viecMuaHang);
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    fillTable();
                    break;
                }
            }
            
        }
    }//GEN-LAST:event_btnXoaMatHangActionPerformed

